const a = 7
let b = 3

b += a  //b = b + a
console.log(b)  // b = 10

b -= 4  // b = b - 4
console.log(b)  // b = 6

b *= 2  // b = b * 2
console.log(b)  // b = 12

b /= 2  // b = b / 2
console.log(b)  // b = 6

b %= 2  // b = b % 2 É o resto da divisão de b com 2 e imprime o resultado
console.log(b)  // b = 0