
// Em JavaScript não tem como fazer um exponencial sem usar a classe 'Math'

/*
 * Calcular a área de uma circunferência. Pi * (area)²
 * Math.PI é uma constante dentro de Math.
 * Math.pow é uma função dentro da classe Math.
 */

const raio = 5.6
const area = Math.PI * Math.pow(raio, 2) //constante PI. pow calcula a potência(power)


console.log(`A área do raio 5.6 é = ${area}`)
console.log(`A área do raio 5.6 é = ${area}`)
console.log(`'Math' é do tipo = ${typeof Math}`)